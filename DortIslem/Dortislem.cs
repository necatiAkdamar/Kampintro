using System;
using System.Collections.Generic;
using System.Text;

namespace DortIslem
{
    class Dortislem
    {
        public int Topla(int a, int b)
        {
            return a + b;
        }
        public int Carp(int a, int b)
        {
            return a * b;
        }
        public int Cikar(int a, int b)
        {
            return a - b;
        }
        public int Bol(int a, int b)
        {
            return a / b;
        }

    }
}



